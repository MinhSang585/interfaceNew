			async: 'true',

			dataType:"json",

			cache: false,

			beforeSend: function() {

				layer.load(1);

			},

			complete: function() {

				layer.closeAll('loading');

			},

			success: function (data) {

				var json = JSON.parse(JSON.stringify(data));

				if(json.status == '<?php echo EXIT_SUCCESS;?>') {

					if(json.code == '<?php echo ANNOUNCEMENT_ON;?>') {

						$('#announcement_icon_'+type).removeClass('fa-volume-up').addClass('fa-volume-mute');

					}else{

						$('#announcement_icon_'+type).removeClass('fa-volume-mute').addClass('fa-volume-up');

					}

				}

			}

		});

	}

